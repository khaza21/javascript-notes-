// Number
let age = 25;
let price = 99.99;

// String
let name = "Khaza";
let greeting = 'Hello, world!';

// Boolean
let isStudent = true;
let isAdmin = false;

// Undefined
let city;
 
// Null
let emptyValue = null;

// Object
let person = {
  name: "Khaza",
  age: 25,
  isStudent: true
};

// Array
let colors = ["red", "green", "blue"];
let numbers = [1, 2, 3, 4, 5];

// Function
function sayHello() {
  return "Hello, Khaza!";
}

// Symbol
let id = Symbol("id");

// BigInt
let bigNumber = 1234567890123456789012345678901234567890n;

// Output All Types
console.log("Number:", age, price);
console.log("String:", name, greeting);
console.log("Boolean:", isStudent, isAdmin);
console.log("Undefined:", city);
console.log("Null:", emptyValue);
console.log("Object:", person);
console.log("Array:", colors, numbers);
console.log("Function:", sayHello());
console.log("Symbol:", id.toString());
console.log("BigInt:", bigNumber);